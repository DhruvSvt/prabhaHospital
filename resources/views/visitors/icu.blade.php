                <h6 class="text-center">What intensive care involves</h6>
                <p>Patients on an ICU will be looked after closely by a team of ICU staff and will be connected to equipment by a number of tubes, wires and cables.</p>
                <p>There will normally be 1 nurse for every 1 or 2 patients</p>
                <p>This equipment is used to monitor their health and support their bodily functions until they recover.</p>
                <p>
                    Equipment that may be used on an ICU includes:
                    <ul>
                        <li><strong>A ventilator –</strong> A machine that helps with breathing; a tube is placed in the mouth, nose or through a small cut in the throat (tracheostomy)</li>
                        <li><strong>Monitoring equipment -</strong> Used to measure important bodily functions, such as heart rate, blood pressure and the level of oxygen in the blood</li>
                        <li><strong>IV lines and pumps -</strong>Tubes inserted into a vein (intravenously) to provide fluids, nutrition and medication</li>
                        <li><strong>Feeding tubes -</strong>Tubes placed in the nose, through a small cut made in the tummy or into a vein if a person is unable to eat normally</li>
                        <li><strong>Drains and catheters -</strong>Drains are tubes used to remove any build-up of blood or fluid from the body; catheters are thin tubes inserted into the bladder to drain pee</li>
